// variables

// number
let num = 10
console.log(`number = ${num}, type = ${typeof num}`)

num = 20
console.log(`number = ${num}, type = ${typeof num}`)

// number
let salary = 15.5
console.log(`salary = ${salary}, type = ${typeof salary}`)

// string
let firstName = 'steve'
let lastName = 'jobs'
let address = `xyz apartment,
abc road,
pqr city.
`
console.log(`firstName = ${firstName}, type = ${typeof firstName}`)
console.log(`lastName = ${lastName}, type = ${typeof lastName}`)
console.log(`address = ${address}, type = ${typeof address}`)

// boolean
let canVote = true
console.log(`canVote = ${canVote}, type = ${typeof canVote}`)

// undefined
// let myVar = undefined
let myVar
console.log(`myVar = ${myVar}, type = ${typeof myVar}`)

// constants
const num2 = 200
console.log(`number = ${num2}, type = ${typeof num2}`)

